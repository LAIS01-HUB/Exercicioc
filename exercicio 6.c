#include <stdio.h> // Biblioteca padrão de entrada e saída

int main() {
    // Declaração da variável i, que será usada como contador
    int i;

    // Laço de repetição que começa em 1 e vai até 100
    for (i = 1; i <= 100; i++) {
        // Imprime o valor de i a cada iteração do laço
        printf("%d\n", i);
    }

    return 0; // Indica que o programa terminou com sucesso
}
